const decode_table = new Map(
	[...encode_table.entries()].map(([key, value]) => [value, key]),
);

export function decode(data: number[]) {
	return data.map((n) => decode_table.get(n)).join("");
}

export function encode(content: string) {
	const buffer = [];
	for (let i = 0; i < content.length; i++) {
		const c = content.codePointAt(i);
		if (c !== undefined) {
			if (c > 0xffff) {
				i++;
			}
			if (c < 0x80) {
				buffer.push(c);
			} else if (c >= 0xff61 && c <= 0xff9f) {
				buffer.push(c - 0xfec0);
			} else {
				const d = encode_table.get(String.fromCodePoint(c)) || 0x3f;
				if (d > 0xff) {
					buffer.push((d >> 8) & 0xff, d & 0xff);
				} else {
					buffer.push(d);
				}
			}
		}
	}
	return Uint8Array.from(buffer);
}
